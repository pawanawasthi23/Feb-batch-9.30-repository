package thisgv;

public class floatint {
public static void main(String[] args) {
	int a=12;
	float f;
	f=a;
	System.out.println("value of a "+a);
	System.out.println("value of f "+f);

	float F1=15.5f;
	int a1;
	a1=(int)F1;
	System.out.println("value of F1 "+F1);
	System.out.println("value of a1 "+a1);
			
	
}
}